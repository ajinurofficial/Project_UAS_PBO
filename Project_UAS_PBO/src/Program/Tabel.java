    private void tblToBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblToBukuMouseClicked
        try {
            // TODO add your handling code here:
            int baris = tblToBuku.getSelectedRow();
            crud.bukaKoneksi();
            ba = crud.readById(Integer.parseInt(tblToBuku.getValueAt(baris, 0).toString()));
            crud.tutupKoneksi();
            txtId.setText(Integer.toString(ba.getId()));
            txtJudul.setText(ba.getJudul());
            txtKategori.setText(ba.getKategori());
            txtPengarang.setText(ba.getPengarang());
            txtPenerbit.setText(ba.getPenerbit());
            txtTahun.setText(Integer.toString(ba.getTahun()));
            txtIsbn.setText(Integer.toString(ba.getIsbn()));
            txtHarga.setText(Integer.toString(ba.getHarga()));
            txtHal.setText(Integer.toString(ba.getJml_hal()));
            txtTerbitan.setText(Integer.toString(ba.getTerbitan()));
 
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Ambil data gagal");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_tblToBukuMouseClicked


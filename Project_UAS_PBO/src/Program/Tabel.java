    private void getAllData() {
        try {
            model.setRowCount(0);
            crud.bukaKoneksi();
            ArrayList<TokoBuku> listTokoBuku = crud.read();
            System.out.println(listTokoBuku.size());
            for (TokoBuku ba : listTokoBuku) {
                model.addRow(new Object[]{
                    ba.getId(),
                    ba.getJudul(),
                    ba.getKategori(),
                    ba.getPengarang(),
                    ba.getPenerbit(),
                    ba.getTahun(),
                    ba.getIsbn(),
                    ba.getHarga(),
                    ba.getJml_hal(),
                    ba.getTerbitan()
                });
            }
            crud.tutupKoneksi();
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Gagal Mengambil Data");
        }
    }
    
    

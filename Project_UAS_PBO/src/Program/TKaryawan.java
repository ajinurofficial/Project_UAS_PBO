    private void getAllData() {
        try {
            model.setRowCount(0);
            crud.bukaKoneksi();
            ArrayList<Karyawan> list = crud.read();
            System.out.println(list.size());
            for (Karyawan ba : list) {
                model.addRow(new Object[]{
                    ba.getId(),
                    ba.getNama(),
                    ba.getAlamat(),
                    ba.getJk(),
                    ba.getEmail(),
                    ba.getTelp(),
                    ba.getGaji(),
                    ba.getJabatan(),
                    ba.getStatus(),
                    ba.getAgama(),
                    ba.getPendidikan(),
                });
            };
            crud.tutupKoneksi();
        } 
        catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Gagal Mengambil Data");
        }
    }
    

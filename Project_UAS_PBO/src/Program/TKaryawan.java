    private void tblKaryawanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKaryawanMouseClicked
        try {
            // TODO add your handling code here:
            int baris = tblKaryawan.getSelectedRow();
            crud.bukaKoneksi();
            ba = crud.readById(Integer.parseInt(tblKaryawan.getValueAt(baris, 0).toString()));
            crud.tutupKoneksi();
            txtId.setText(Integer.toString(ba.getId()));
            txtNama.setText(ba.getNama());
            txtAlamat.setText(ba.getAlamat());
            txtJk.setText(ba.getJk());
            txtEmail.setText(ba.getEmail());
            txtTelp.setText(ba.getTelp());
            txtGaji.setText(Integer.toString(ba.getGaji()));
            txtJabatan.setText(ba.getJabatan());
            txtStatus.setText(ba.getStatus());
            txtAgama.setText(ba.getAgama());
            txtPendidikan.setText(ba.getPendidikan());

        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Ambil data gagal");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_tblKaryawanMouseClicked


                    karyawan.setJk(txtJk.getText());
                    karyawan.setEmail(txtEmail.getText());
                    karyawan.setTelp(txtTelp.getText());
                    karyawan.setGaji(Integer.parseInt(txtGaji.getText()));
                    karyawan.setJabatan((txtJabatan.getText()));
                    karyawan.setStatus((txtStatus.getText()));
                    karyawan.setAgama((txtAgama.getText()));
                    karyawan.setPendidikan((txtPendidikan.getText()));
                    crud.update(karyawan);
                    getAllData();
                    readData();
                    crud.tutupKoneksi();
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
